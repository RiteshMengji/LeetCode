package com.leetcode.util;

public class ArrayUtil {

	public static void printArray(int[] arr){
		for(int n : arr){
			System.out.print(n+" ");
		}
		System.out.println("");
	}
	
	public static void printArray(String[] arr){
		for(String n : arr){
			System.out.print(n+" ");
		}
		System.out.println("");
	}
}
